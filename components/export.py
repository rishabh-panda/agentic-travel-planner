def export_to_pdf(itinerary: str, destination: str, days: int, budget: float,
                  currency: str, budget_info: Optional[Dict[str, Any]] = None,
                  weather_info: Optional[Dict[str, Any]] = None) -> bytes:
    """
    Export itinerary to professionally formatted PDF with company branding.
    
    Args:
        itinerary: The full itinerary text
        destination: Travel destination
        days: Number of days
        budget: Total budget
        currency: Currency code
        budget_info: Optional budget breakdown information
        weather_info: Optional weather information
    
    Returns:
        PDF content as bytes (base64 encoded for Streamlit download)
    """
    try:
        # Try to import PDF generation libraries
        try:
            from reportlab.lib import colors
            from reportlab.lib.pagesizes import letter, A4
            from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
            from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
            from reportlab.lib.units import inch
            from reportlab.pdfgen import canvas
            from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT
            HAS_REPORTLAB = True
        except ImportError:
            HAS_REPORTLAB = False
        
        if not HAS_REPORTLAB:
            # Fallback: Create HTML that can be printed to PDF
            return _export_to_pdf_html(itinerary, destination, days, budget, 
                                       currency, budget_info, weather_info)
        
        # Create PDF using ReportLab
        buffer = tempfile.NamedTemporaryFile(suffix='.pdf', delete=False)
        buffer.close()
        
        doc = SimpleDocTemplate(
            buffer.name,
            pagesize=letter,
            rightMargin=0.5*inch,
            leftMargin=0.5*inch,
            topMargin=0.5*inch,
            bottomMargin=0.5*inch
        )
        
        # Define styles
        styles = getSampleStyleSheet()
        
        # Custom styles
        title_style = ParagraphStyle(
            'CustomTitle',
            parent=styles['Heading1'],
            fontSize=24,
            textColor=colors.Color(0.118, 0.227, 0.373),  # #1E3A5F
            alignment=TA_CENTER,
            spaceAfter=30
        )
        
        subtitle_style = ParagraphStyle(
            'CustomSubtitle',
            parent=styles['Heading2'],
            fontSize=14,
            textColor=colors.Color(0.0, 0.502, 0.502),  # #008080
            alignment=TA_CENTER,
            spaceAfter=20
        )
        
        header_style = ParagraphStyle(
            'CustomHeader',
            parent=styles['Heading2'],
            fontSize=16,
            textColor=colors.Color(0.118, 0.227, 0.373),
            spaceAfter=10,
            spaceBefore=20
        )
        
        body_style = ParagraphStyle(
            'CustomBody',
            parent=styles['BodyText'],
            fontSize=11,
            leading=14,
            spaceAfter=10
        )
        
        # Build PDF content
        story = []
        
        # Header
        story.append(Paragraph("Agentic Travel Itinerary Planner", title_style))
        story.append(Paragraph("Professional Travel Plan", subtitle_style))
        story.append(Spacer(1, 0.5*inch))
        
        # Trip Information
        story.append(Paragraph("Trip Information", header_style))
        
        trip_data = [
            ['Destination:', destination],
            ['Duration:', f"{days} day{'s' if days > 1 else ''}"],
            ['Total Budget:', format_currency(budget, currency)],
            ['Export Date:', datetime.now().strftime('%Y-%m-%d %H:%M:%S')]
        ]
        
        trip_table = Table(trip_data, colWidths=[1.5*inch, 4*inch])
        trip_table.setStyle(TableStyle([
            ('FONTNAME', (0, 0), (0, -1), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 11),
            ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
            ('TOPPADDING', (0, 0), (-1, -1), 6),
        ]))
        story.append(trip_table)
        story.append(Spacer(1, 0.25*inch))
        
        # Budget Summary (if available)
        if budget_info:
            story.append(Paragraph("Budget Summary", header_style))
            
            breakdown = budget_info.get('breakdown', {})
            if breakdown:
                budget_data = [['Category', 'Amount']]
                for cat, amt in list(breakdown.items())[:5]:
                    budget_data.append([cat.capitalize(), format_currency(amt, currency)])
                
                budget_table = Table(budget_data, colWidths=[2.5*inch, 2.5*inch])
                budget_table.setStyle(TableStyle([
                    ('FONTNAME', (0, 0), (0, -1), 'Helvetica-Bold'),
                    ('FONTSIZE', (0, 0), (-1, -1), 10),
                    ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
                    ('TOPPADDING', (0, 0), (-1, -1), 6),
                    ('BACKGROUND', (0, 0), (0, 0), colors.Color(0.118, 0.227, 0.373)),
                    ('TEXTCOLOR', (0, 0), (0, 0), colors.white),
                ]))
                story.append(budget_table)
                story.append(Spacer(1, 0.25*inch))
        
        # Weather Summary (if available)
        if weather_info:
            forecast = weather_info.get('forecast', [])
            if forecast:
                story.append(Paragraph("Weather Forecast", header_style))
                
                weather_data = [['Date', 'Condition', 'Temperature']]
                for day in forecast[:5]:
                    weather_data.append([
                        day.get('date', 'N/A'),
                        day.get('condition', 'N/A'),
                        f"{day.get('temp', 'N/A')}°C"
                    ])
                
                weather_table = Table(weather_data, colWidths=[1.5*inch, 2*inch, 1.5*inch])
                weather_table.setStyle(TableStyle([
                    ('FONTNAME', (0, 0), (0, -1), 'Helvetica-Bold'),
                    ('FONTSIZE', (0, 0), (-1, -1), 9),
                    ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
                    ('TOPPADDING', (0, 0), (-1, -1), 6),
                ]))
                story.append(weather_table)
                story.append(Spacer(1, 0.25*inch))
        
        # Itinerary Content
        story.append(Paragraph("Detailed Itinerary", header_style))
        
        # Split itinerary into paragraphs and add to story
        paragraphs = itinerary.split('\n\n')
        for para in paragraphs[:20]:  # Limit to first 20 paragraphs for PDF
            if para.strip():
                story.append(Paragraph(para.strip(), body_style))
        
        if len(paragraphs) > 20:
            story.append(Paragraph("[... Itinerary continues in full version ...]", body_style))
        
        story.append(Spacer(1, 0.5*inch))
        
        # Footer
        footer_text = "Generated by Agentic Travel Itinerary Planner | Professional travel planning powered by AI"
        story.append(Paragraph(footer_text, ParagraphStyle(
            'Footer',
            parent=styles['BodyText'],
            fontSize=9,
            textColor=colors.grey,
            alignment=TA_CENTER
        )))
        
        # Build PDF
        doc.build(story)
        
        # Read PDF content
        with open(buffer.name, 'rb') as f:
            pdf_content = f.read()
        
        # Clean up
        os.unlink(buffer.name)
        
        return pdf_content
        
    except Exception as e:
        print(f"PDF export error: {e}")
        # Fallback to HTML
        return _export_to_pdf_html(itinerary, destination, days, budget, 
                                   currency, budget_info, weather_info)


